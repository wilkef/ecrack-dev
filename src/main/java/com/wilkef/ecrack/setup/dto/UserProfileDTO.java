package com.wilkef.ecrack.setup.dto;

/**
 * The Class UserProfileDTO.
 */
public class UserProfileDTO {
	
	
	/** The data output. */
	private String dataOutput;
	
	/** The update count. */
	private Integer updateCount;

	/**
	 * Gets the update count.
	 *
	 * @return the update count
	 */
	public Integer getUpdateCount() {
		return updateCount;
	}

	/**
	 * Sets the update count.
	 *
	 * @param updateCount the new update count
	 */
	public void setUpdateCount(Integer updateCount) {
		this.updateCount = updateCount;
	}

	/**
	 * Gets the data output.
	 *
	 * @return the data output
	 */
	public String getDataOutput() {
		return dataOutput;
	}

	/**
	 * Sets the data output.
	 *
	 * @param dataOutput the new data output
	 */
	public void setDataOutput(String dataOutput) {
		this.dataOutput = dataOutput;
	}
	
}
